/**
 * Pulls stage_01..stage_10 from orn1983/battlecityjs (NES Battle City layout clone)
 * and writes src/game-center/tank90/nesTerrainStrings.ts — run after changing upstream reference.
 * Encoding: 0 brick, 1 steel, 2 water, 3 trees, 4 ice, 5 empty.
 */
import fs from 'node:fs'
import vm from 'node:vm'
import { fileURLToPath } from 'node:url'

const OUT = fileURLToPath(new URL('../src/game-center/tank90/nesTerrainStrings.ts', import.meta.url))
const SRC = 'https://raw.githubusercontent.com/orn1983/battlecityjs/master/levels.js'

const numToChar = (n) => {
  if (n === 0) return 'B'
  if (n === 1) return 'S'
  if (n === 2) return 'W'
  if (n === 3) return 'G'
  if (n === 4) return 'I'
  return '.'
}

const text = await fetch(SRC).then((r) => {
  if (!r.ok) throw new Error(`fetch ${SRC}: ${r.status}`)
  return r.text()
})

const ctx = {}
vm.createContext(ctx)
vm.runInContext(text, ctx)
const gl = ctx.g_levels
if (!gl) throw new Error('g_levels missing after eval')

let out = `// AUTO-GENERATED by scripts/sync-nes-battlecity-terrain.mjs — do not edit by hand.\n`
out += `// Source: ${SRC}\n`
out += `// Tile codes: B brick, S steel, W water, G trees, I ice, . empty\n\n`

for (let s = 1; s <= 10; s += 1) {
  const key = `stage_${String(s).padStart(2, '0')}`
  const grid = gl[key]
  if (!grid || grid.length !== 26 || grid.some((row) => row.length !== 26)) {
    throw new Error(`Bad grid ${key}`)
  }
  const rows = grid.map((row) => row.map(numToChar).join(''))
  out += `export const NES_STAGE_${String(s).padStart(2, '0')}_ROWS: string[] = [\n`
  for (const line of rows) {
    out += `  '${line}',\n`
  }
  out += `]\n\n`
}

fs.writeFileSync(OUT, out, 'utf8')
console.log('wrote', OUT)
